// Utility function to get a random prompt based on category and level
export const getRandomPrompt = (category = null, level = 'foundation') => {
  const categories = Object.keys(promptCategories);
  const selectedCategory = category || categories[Math.floor(Math.random() * categories.length)];

  const prompts = promptCategories[selectedCategory]?.prompts[level] ||
                  promptCategories.personal.prompts.foundation;

  const randomPrompt = prompts[Math.floor(Math.random() * prompts.length)];

  return {
    prompt: randomPrompt,
    category: selectedCategory,
    level: level,
    categoryName: promptCategories[selectedCategory]?.name || 'Personal'
  };
};

// Get prompts by specific category and level
export const getPromptsByCategory = (category, level = 'foundation') => {
  return promptCategories[category]?.prompts[level] || [];
};

// Get all categories
export const getCategories = () => {
  return Object.keys(promptCategories).map(key => ({
    id: key,
    name: promptCategories[key].name,
    icon: promptCategories[key].icon
  }));
};

// Get random prompts avoiding recent ones
export const getRandomPromptWithHistory = (recentPrompts = [], category = null, level = 'foundation', maxRetries = 10) => {
  let attempts = 0;
  let prompt;

  do {
    prompt = getRandomPrompt(category, level);
    attempts++;
  } while (recentPrompts.includes(prompt.prompt) && attempts < maxRetries);

  return prompt;
};